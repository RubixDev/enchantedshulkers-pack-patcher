<div>
    <h1>EnchantedShulkers Pack Patcher</h1>
    <p>
        When using <a href="https://modrinth.com/mod/enchantedshulkers"
            >EnchantedShulkers</a
        > with a resource pack that alters the look of Shulker Boxes and/or Ender Chests, the
        enchanted versions of those items and blocks will still use the vanilla texture while
        closed. This had to be done to fix overlapping glint effects on said blocks.
    </p>
    <p>
        On this page you can patch any resource pack to provide the required textures to
        EnchantedShulkers, so that closed enchanted containers also use the resource
        pack's textures instead of the vanilla ones. To do that, simply select your
        resource pack with the 'Choose File' button below and then click the 'Patch'
        button.
    </p>
</div>
